    //Elige una palabra aleatoria
    private String eligePalabraOculta( String [] lista){
        Random aleatorio = new Random();
        return lista[ aleatorio.nextInt(lista.length) ];
    }
     
    
    //Auxiliar es, Auxiliar igual a Auxiliar mas "_ " tantas veces como letras 
    //tenga la palabra oculta
    private void escribeGuionesEnPantalla(){
        String auxiliar = ""; //creamos un auxiliar
        for (int i=0; i< palabraOculta.length(); i++){
            auxiliar = auxiliar + "_ ";
        }
        Pantalla.setText(auxiliar); //guiones del auxiliar
    }
    
 
    private void chequeaLetra(String letra){
        letra = letra.toUpperCase();
        
        StringBuilder palabraConGuiones = new StringBuilder(Pantalla.getText());
        if (palabraOculta.contains(letra)){
            for (int i=0; i< palabraOculta.length(); i++){
                if (palabraOculta.charAt(i) == letra.charAt(0)){
                    palabraConGuiones.setCharAt(2*i,letra.charAt(0));
                }
            }
            Pantalla.setText(palabraConGuiones.toString());
        }
        else{
            numeroFallos++;//me incrementa el numero de fallos en 1
            
        }
        dibujaImagen(numeroFallos); //si falla me saca una imagen
    }

    private void dibujaImagen(int numeroImagen){
        
        URL nombreImagen = null;
        switch (numeroImagen){
            case 0: nombreImagen = getClass().getResource("/imagenes/Ahorcado1.jpg"); break;
            case 1: nombreImagen = getClass().getResource("/imagenes/Ahorcado2.jpg"); break;
            case 2: nombreImagen = getClass().getResource("/imagenes/Ahorcado3.jpg"); break;
            case 3: nombreImagen = getClass().getResource("/imagenes/Ahorcado4.jpg"); break;
            case 4: nombreImagen = getClass().getResource("/imagenes/Ahorcado5.jpg"); break;
            case 5: nombreImagen = getClass().getResource("/imagenes/Ahorcado6.jpg"); break;
            case 6: nombreImagen = getClass().getResource("/imagenes/Ahorcado7.jpg"); break;
            default: nombreImagen = getClass().getResource("/imagenes/AhorcadoFin.jpg");
        }
        
        int ancho = panelAhorcado.getWidth();
        int alto = panelAhorcado.getHeight();
        
        ImageIcon miImagen = new ImageIcon (new ImageIcon(nombreImagen).getImage().getScaledInstance(ancho, alto,Image.SCALE_DEFAULT));
        panelAhorcado.setIcon(miImagen);
    }
    
    private void chequeaBoton (JButton miBoton){
        //si no has perdido la partida siguen igual, y sino se desactivan
        miBoton.setEnabled(false);
        chequeaLetra(miBoton.getText());
        
    }
